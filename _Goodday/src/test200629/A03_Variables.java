package test200629;

public class A03_Variables {

	public static void main(String[] args) {
		/* 변수를 활용한 간단한 계산
		 * 1. 정수형 변수 number를 선언하여 정수 대입
		 * 2. 정수형 변수 result 선언
		 * 3. number에 10을 더하여 result 변수에 대입
		 * 4. result 변수가 가지고 있는 값 출력 */
		
		int number = 1;
		int result;
	// int number, result 이런식으로 선언 가능
		result = number+10;
		System.out.println(result);
		
		double KEB, LJS;
		KEB = 0;
		LJS = 0;
		KEB = 9;
		LJS = KEB - 1;
		System.out.println(LJS);
		
		double good_girl;
		good_girl = 12;
		System.out.println(good_girl);
		
		
		float good;
		good = 12f;
		System.out.println(good);
		
		
	
		
	

	}

}
